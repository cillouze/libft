#include "libft.h"

long long int	ft_atoi(char *s1)
{
	char	nega;
	long long int	nb;

	nb = 0;
	nega = '+';
	while ((*s1 == '\t' || *s1 == '\n' || *s1 == '\f' 
			|| *s1 == ' ' || *s1 == '\r' || *s1 == '\v') && *s1)
		s1++;
	if (*s1 == '-' || *s1 == '+' )
	{
		nega = *s1; 
		s1++;
	}
	while (*s1 >= '0' && *s1 <= '9' && *s1)
	{
		nb = nb * 10 + (*s1 - '0');
		s1++;
		if (((nb == 922337203685477580 && *s1 > '7')
				|| ( nb > 922337203685477580 && *s1 != '\0'))
				&& (nega == '-'))
			return (0);
		else if ((nb == 922337203685477580 && *s1 > '7')
				|| ( nb > 922337203685477580 && *s1 != '\0'))
			return (-1);
	}
	return (nega == '-' ? -nb : nb);
}
